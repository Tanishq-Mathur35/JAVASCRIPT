// While loop example

let count = 1;
while (count <= 10) {
    console.log(count);
    count++;
}


let arr = 0;
let myArray = ["apple", "banana", "cherry", "date", "elderberry"];
while (arr < myArray.length) {
    console.log(`Element at index ${arr}: ${myArray[arr]}`);
    arr++;
}



// Do-while loop example

let score = 1;
do {
    console.log(`Score: ${score}`);
    score++;
} while (score <= 10);
